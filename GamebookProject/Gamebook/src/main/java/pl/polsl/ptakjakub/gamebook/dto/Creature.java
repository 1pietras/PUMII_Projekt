package pl.polsl.ptakjakub.gamebook.dto;

/**
 * Represents creature in game.
 *
 * @author Jakub Ptak
 * @version 1.0
 */
public class Creature {

    private int id;

    private String name;

    private int agility;

    private int vitality;

    /**
     * Gets id of a creature.
     *
     * @return id
     */
    public int getId() {
        return id;
    }

    /**
     * Sets id of a creature.
     *
     * @param id number
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Gets name of a creature.
     *
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name of a creature.
     *
     * @param name String
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets agility attribute value of a creature.
     *
     * @return agility
     */
    public int getAgility() {
        return agility;
    }

    /**
     * Sets agility attriute value of a creature.
     *
     * @param agility value
     */
    public void setAgility(int agility) {
        this.agility = agility;
    }

    /**
     * Gets vitality attribute value of a creature.
     *
     * @return vitality
     */
    public int getVitality() {
        return vitality;
    }

    /**
     * Sets vitality attribute value of a creature.
     *
     * @param vitality value
     */
    public void setVitality(int vitality) {
        this.vitality = vitality;
    }
}

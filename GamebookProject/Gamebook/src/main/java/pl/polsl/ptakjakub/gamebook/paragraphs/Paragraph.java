package pl.polsl.ptakjakub.gamebook.paragraphs;

/**
 * Represents a basic paragraphs.
 *
 * @author Jakub Ptak
 * @version 1.0
 */
public class Paragraph {

    private int id;

    private String text;

    /**
     * Retrieves id of a paragraph.
     *
     * @return id
     */
    public int getId() {
        return id;
    }

    /**
     * Sets an id of a paragraph.
     *
     * @param id paragraph id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Retrieves text of a paragraph.
     *
     * @return text
     */
    public String getText() {
        return text;
    }

    /**
     * Sets an text of a paragraph.
     *
     * @param text paragraph text
     */
    public void setText(String text) {
        this.text = text;
    }
}
